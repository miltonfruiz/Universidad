# Ejercicio N°4

# Dados dos números enteros obtener su suma, resta, multiplicación y división.

# var nro1, nro2, suma, resta, multi: integer; division: float;

nro1 = int(input('Ingrese el primer número: '))
nro2 = int(input('Ingrese el segundo número: '))
suma = nro1 + nro2
resta = nro1 - nro2
multi = nro1 * nro2
division = nro1 / nro2
print('La suma es',suma,',la resta es',resta,',la multiplicación es',multi,'y la división es',division)