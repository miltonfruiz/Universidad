#Ejercicio N°3

# Dados como datos cinco números obtener el promedio de los mismos e informar el
# resultado.

# var nro1, nro2, nro3, nro4, nro5: integer; promedio: float;

nro1 = int(input('Ingrese el primer número: '))
nro2 = int(input('Ingrese el segundo número: '))
nro3 = int(input('Ingrese el tercer número: '))
nro4 = int(input('Ingrese el cuerto número: '))
nro5 = int(input('Ingrese el quinto número: '))
promedio = (nro1 + nro2 + nro3 + nro4 + nro5) / 5
print('El retultado es: ',promedio)